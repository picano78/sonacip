def get_user_language():
    """Get the current user's language preference.
    Priority: session > user.language > default (Italian).
    """
    try:
        lang = session.get('language')
        if lang and lang in SUPPORTED_LANGUAGES:
            return lang
    except Exception:
        pass

    try:
        if current_user and current_user.is_authenticated:
            user_lang = getattr(current_user, 'language', None)
            if user_lang and user_lang in SUPPORTED_LANGUAGES:
                return user_lang
    except Exception:
        pass

    return DEFAULT_LANGUAGE


def get_translation(key, lang=None):
    """Get a translation for a key in the specified language.
    Falls back to Italian if the key is not found in the requested language.
    """
    if lang is None:
        lang = get_user_language()
    if lang not in SUPPORTED_LANGUAGES:
        lang = DEFAULT_LANGUAGE
    translations = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANGUAGE])
    result = translations.get(key)
    if result is None:
        result = TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    return result


def t(key, lang=None):
    """Shorthand for get_translation."""
    return get_translation(key, lang)
